typedef signed long				LONG;
typedef signed long				SI_32;
typedef unsigned long			ULONG;
typedef unsigned long			UI_32;
typedef double					DOUBLE;
typedef float					FLOAT;
typedef boolean                 BOOL;

#endif // _MY_SYSTYPE_H_
